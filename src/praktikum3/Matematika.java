package praktikum3;

public class Matematika {
    void pertambahan(double x, double y) {
        double hasil = x + y;
        System.out.println("Pertambahan: " + x + " + " + y + " = " + hasil);
    }

    void pengurangan(double x, double y) {
        double hasil = x - y;
        System.out.println("Pengurangan: " + x + " - " + y + " = " + hasil);
    }

    void perkalian(double x, double y) {
        double hasil = x * y;
        System.out.println("Perkalian: " + x + " x " + y + " = " + hasil);
    }

    void pembagian(double x, double y) {
        double hasil = x / y;
        System.out.println("Pembagian: " + x + " / " + y + " = " + hasil);
    }
}
